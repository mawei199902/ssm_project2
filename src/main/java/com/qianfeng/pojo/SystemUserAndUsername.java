package com.qianfeng.pojo;

public class SystemUserAndUsername {

    private int user_id;
    private String user_true_name;
    private String user_tel;
    private String user_sex;
    private String login_user_name;
    private String login_pass_word;


    public int getUser_id() {
        return user_id;
    }

    public void setUser_id(int user_id) {
        this.user_id = user_id;
    }

    public String getUser_true_name() {
        return user_true_name;
    }

    public void setUser_true_name(String user_true_name) {
        this.user_true_name = user_true_name;
    }

    public String getUser_tel() {
        return user_tel;
    }

    public void setUser_tel(String user_tel) {
        this.user_tel = user_tel;
    }

    public String getUser_sex() {
        return user_sex;
    }

    public void setUser_sex(String user_sex) {
        this.user_sex = user_sex;
    }

    public String getLogin_user_name() {
        return login_user_name;
    }

    public void setLogin_user_name(String login_user_name) {
        this.login_user_name = login_user_name;
    }

    public String getLogin_pass_word() {
        return login_pass_word;
    }

    public void setLogin_pass_word(String login_pass_word) {
        this.login_pass_word = login_pass_word;
    }


}
